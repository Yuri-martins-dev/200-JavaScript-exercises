// Declare duas variáveis e atribua valores booleanos a elas. Use os operadores lógicos AND, OR e NOT para realizar operações lógicas entre as variáveis. Imprima os resultados.
let valorA = true;
let valorB = false;
let conjunção = (valorA && valorB);
let disjunção = (valorA || valorB);
console.log ("Conjunção: " + conjunção);
console.log ("Disjunção: " + disjunção);
console.log ("Negação: " + !valorA);
console.log ("Negação: " + !valorB);